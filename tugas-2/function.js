function introduce(name, age, address, hobby) {
  return `Nama saya ${name}, umur saya ${age} tahun, alamat saya di ${address}, dan saya punya hobby yaitu ${hobby}!`;
}

// TEST CASE
console.log(
  introduce("Yazid Aqsa", 23, "Bekasi", "Belajar bahasa jepang dan Gaming")
);
// Output: Nama saya Agus, umur saya 30 tahun, alamat saya di Jogja, dan saya punya hobby yaitu Gaming!
